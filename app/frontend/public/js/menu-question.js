const questionListContent = document.querySelector('#questionListContent');
const questionTemplate = document.querySelector('#questionDisplayTemplate');
const questionForm = document.querySelector("#questionForm");
const aiForm = document.querySelector('#aiForm');

// 1. Critical: Run this on page load to fill the left pane
document.addEventListener("DOMContentLoaded", () => {
    populateQuestions();
});

/**
 * Populate the left pane with questions from the database
 */
async function populateQuestions() {
    // Clear current content
    questionListContent.innerHTML = "";

    const res = await fetch(`/api/questions`);

    if (res.status !== 200) {
        console.error("Failed to fetch questions");
        return;
    }

    const questions = await res.json();

    questions.forEach(question => {
        // Clone the template
        const instance = questionTemplate.content.cloneNode(true);
        
        // Use the wrapper for "in-place" swapping
        const container = document.createElement('div');
        container.className = "question-card-wrapper";
        
        // Fill template data
        instance.querySelector('.question').textContent = question.question;
        instance.querySelector('.correctAnswer').textContent = `Correct: ${question.corrAnswer}`;
        instance.querySelector('.wrongAnswer1').textContent = question.incorrONE;
        instance.querySelector('.wrongAnswer2').textContent = question.incorrTWO;
        instance.querySelector('.wrongAnswer3').textContent = question.incorrTHREE;

        // Category Mapping
        const categories = ["", "History & Evolution", "Technical Aspects", "Sustainability", "Consumerism & Ethics", "End-of-Life & Data", "Logistics & Distribution"];
        instance.querySelector('.category-label').textContent = categories[question.category] || "General";
        
        // Note: Ensure your HTML template has an element with class 'ai' if you want to use this
        const aiStatus = instance.querySelector('.ai');
        if (aiStatus) {
            aiStatus.textContent = question.isAI === 1 ? "Generated by AI" : "Manual Entry";
        }

        // Attach functionality to the EXISTING buttons in the template
        const deleteBtn = instance.querySelector('.delete-btn');
        const editBtn = instance.querySelector('.edit-btn');

        if (deleteBtn) {
            deleteBtn.onclick = () => deleteQuestion(question.questionID);
        }
        
        if (editBtn) {
            editBtn.onclick = () => renderInPlaceEdit(container, question);
        }

        container.appendChild(instance);
        questionListContent.appendChild(container);
    });
}

/**
 * Swaps the display card for an edit form inside the left pane
 */
function renderInPlaceEdit(container, question) {
    container.innerHTML = `
        <div class="bg-white p-3 rounded mb-3 border border-info text-start">
            <h5 class="text-dark">Edit Question</h5>
            <input type="text" class="form-control mb-2 edit-q" value="${question.question}">
            <label class="small fw-bold">Correct Answer</label>
            <input type="text" class="form-control mb-2 edit-corr border-success" value="${question.corrAnswer}">
            <label class="small fw-bold">Incorrect Answers</label>
            <input type="text" class="form-control mb-1 edit-w1" value="${question.incorrONE}">
            <input type="text" class="form-control mb-1 edit-w2" value="${question.incorrTWO}">
            <input type="text" class="form-control mb-2 edit-w3" value="${question.incorrTHREE}">
            <div class="d-flex gap-2">
                <button class="btn btn-success btn-sm save-btn">Save</button>
                <button class="btn btn-secondary btn-sm cancel-btn">Cancel</button>
            </div>
        </div>
    `;

    container.querySelector('.cancel-btn').onclick = () => populateQuestions(); // Refresh list to cancel
    
    container.querySelector('.save-btn').onclick = async () => {
        const updatedData = {
            questionId: question.questionID,
            questionData: {
                question: container.querySelector('.edit-q').value,
                correctAnswer: container.querySelector('.edit-corr').value,
                wrongAnswer1: container.querySelector('.edit-w1').value,
                wrongAnswer2: container.querySelector('.edit-w2').value,
                wrongAnswer3: container.querySelector('.edit-w3').value,
                category: question.category,
                ai: question.isAI
            }
        };

        const res = await fetch(`/api/questions`, { 
            method: "PUT",  
            headers: {'Content-Type': 'application/json'},  
            body: JSON.stringify(updatedData)
        });

        if (res.ok) {
            populateQuestions();
        }
    };
}

/**
 * Delete and Refresh
 */
async function deleteQuestion(id) {
    if (!confirm("Are you sure you want to delete this question?")) return;
    
    const res = await fetch(`/api/questions`, { 
        method: "DELETE",  
        headers: {'Content-Type': 'application/json'},  
        body: JSON.stringify({ questionId: id })
    });
   
    if (res.ok) populateQuestions();
}

// AI FORM Logic (Updated to point to your manual form inputs)
aiForm.addEventListener('submit', async (event) => {
    event.preventDefault();
    const aiPrompt = document.querySelector("#aiPrompt");

    const res = await fetch(`/api/ai/gemini`, { 
        method: "POST",  
        headers: {'Content-Type': 'application/json'},  
        body: JSON.stringify({ aiPrompt: aiPrompt.value })
    });
    
    const question = await res.json();
    if (question.error) {
        alert(question.error);
    } else {
        // Fill the manual form
        document.querySelector("#question").value = question.question;
        document.querySelector("#correctAnswer").value = question.corrAnswer;
        document.querySelector("#wrongAnswer1").value = question.incorrAnswer1;
        document.querySelector("#wrongAnswer2").value = question.incorrAnswer2;
        document.querySelector("#wrongAnswer3").value = question.incorrAnswer3;
        document.querySelector('#category').value = question.category;
        document.querySelector("#ai").value = 1;
    }
    aiPrompt.value = "";
});

// Manual Form Submission
questionForm.addEventListener("submit", async (e) => {
    e.preventDefault();
    const formData = new FormData(questionForm);
    const data = {
        question: formData.get("question"),
        correctAnswer: formData.get("correctAnswer"),
        wrongAnswer1: formData.get("wrongAnswer1"),
        wrongAnswer2: formData.get("wrongAnswer2"),
        wrongAnswer3: formData.get("wrongAnswer3"),
        category: Number(formData.get("category")),
        ai: Number(formData.get("ai"))
    };

    const res = await fetch("/api/questions", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(data)
    });

    if (res.ok) {
        questionForm.reset();
        populateQuestions(); // Refresh the left pane immediately
    }
});